def calculate_store_value():
    """Расчет стоимости товаров на складе"""
    goods = {
        'Лампа': '12345',
        'Стол': '23456', 
        'Диван': '34567',
        'Стул': '45678',
    }
    
    store = {
        '12345': [{'quantity': 27, 'price': 42}],
        '23456': [{'quantity': 22, 'price': 510}, {'quantity': 32, 'price': 520}],
        '34567': [{'quantity': 2, 'price': 1200}, {'quantity': 1, 'price': 1150}],
        '45678': [{'quantity': 50, 'price': 100}, {'quantity': 12, 'price': 95}, {'quantity': 43, 'price': 97}],
    }
    
    results = {}
    
    # Лампа (один элемент в списке)
    lamp_items = store[goods['Лампа']]
    lamp_quantity = lamp_items[0]['quantity']
    lamp_cost = lamp_items[0]['quantity'] * lamp_items[0]['price']
    results['Лампа'] = (lamp_quantity, lamp_cost)
    print(f'Лампа - {lamp_quantity} шт, стоимость {lamp_cost} руб')
    
    # Стол (два элемента в списке)
    table_items = store[goods['Стол']]
    table_quantity = table_items[0]['quantity'] + table_items[1]['quantity']
    table_cost = (table_items[0]['quantity'] * table_items[0]['price'] + 
                 table_items[1]['quantity'] * table_items[1]['price'])
    results['Стол'] = (table_quantity, table_cost)
    print(f'Стол - {table_quantity} шт, стоимость {table_cost} руб')
    
    # Диван (два элемента в списке)
    sofa_items = store[goods['Диван']]
    sofa_quantity = sofa_items[0]['quantity'] + sofa_items[1]['quantity']
    sofa_cost = (sofa_items[0]['quantity'] * sofa_items[0]['price'] + 
                sofa_items[1]['quantity'] * sofa_items[1]['price'])
    results['Диван'] = (sofa_quantity, sofa_cost)
    print(f'Диван - {sofa_quantity} шт, стоимость {sofa_cost} руб')
    
    # Стул (три элемента в списке)
    chair_items = store[goods['Стул']]
    chair_quantity = chair_items[0]['quantity'] + chair_items[1]['quantity'] + chair_items[2]['quantity']
    chair_cost = (chair_items[0]['quantity'] * chair_items[0]['price'] + 
                 chair_items[1]['quantity'] * chair_items[1]['price'] + 
                 chair_items[2]['quantity'] * chair_items[2]['price'])
    results['Стул'] = (chair_quantity, chair_cost)
    print(f'Стул - {chair_quantity} шт, стоимость {chair_cost} руб')
    
    return results